# liczby całkowite
x = 10
print("x =", x)
print("x + 5 =", x + 5) # dodawanie
print("x - 3 =", x- 3) # odejmowanie
print("x * 2 =", x * 2) # mnożenie
print("x / 4 +", x / 4) # dzielenie
print("x // 4 =", x // 4)# całkowitoliczbowe
print("x % 4 =", x % 4) # reszta
print("x ** 2 =", x ** 2) # potęgowanie

